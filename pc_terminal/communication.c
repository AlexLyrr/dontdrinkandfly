/**
 * @author Joseph Verburg
 */
void initializeCommunication() {
    lastPacketNum = 0;
    for(int i = 0; i < 65535; i++){
        sPacketBuffer[i].fcs = i;
        for(int o = 0; o < 10; o++) {
            sPacketBuffer[i].payload[o] = 0;
        }
        sPacketBuffer[i].crc = 0;
		receivedACK[i] = false;
	}
}

/**
 * @author Joseph Verburg
 */
void writeByte(uint8_t byte) {
    rs232_putchar((char)byte);
}

/**
 * @author Joseph Verburg
 */
void writePing(uint64_t clk) {
    writePacket(
        12, 
        (clk >> 56) & 0xFF,
        (clk >> 48) & 0xFF,
        (clk >> 40) & 0xFF,
        (clk >> 32) & 0xFF,
        (clk >> 24) & 0xFF,
        (clk >> 16) & 0xFF,
        (clk >> 8) & 0xFF,
        clk & 0xFF,
        0
    );
}


/**
 * @author Joseph Verburg
 */
void writePong(uint64_t clk) {
     writePacket(
        13, 
        (clk >> 56) & 0xFF,
        (clk >> 48) & 0xFF,
        (clk >> 40) & 0xFF,
        (clk >> 32) & 0xFF,
        (clk >> 24) & 0xFF,
        (clk >> 16) & 0xFF,
        (clk >> 8) & 0xFF,
        clk & 0xFF,
        0
    );
}


/**
 * @author Joseph Verburg
 */
void writeSetMode(uint8_t mode) {
    writePacket(5, mode & 0x0F, 0,0,0,0,0,0,0,0);
}
/**
 * @author Joseph Verburg
 */
void writeSetControl(bool abort, uint8_t roll, uint8_t pitch, uint8_t yaw, uint16_t lift) {
    writePacket(3, abort ? 0x80 : 0x00, roll, pitch, yaw, (lift >> 8) & 0xFF, lift & 0xFF, 0, 0, 0);
}
/**
 * @author Joseph Verburg
 */
void writeSetP(uint16_t roll, uint16_t pitch, uint16_t yaw) {
    writePacket(9, (roll >> 8) & 0xFF, roll & 0xFF, (pitch >> 8) & 0xFF, pitch & 0xFF, (yaw >> 8) & 0xFF, yaw & 0xFF, 0, 0, 0);
}


/**
 * @author Joseph Verburg
 */
void writePacket(uint8_t b0, uint8_t b1, uint8_t b2, uint8_t b3, uint8_t b4, uint8_t b5, uint8_t b6, uint8_t b7, uint8_t b8, uint8_t b9) {

    uint16_t packetNumber = lastPacketNum++;
    uint8_t crc = 0x00;
	crc = crc8_table[crc ^ (packetNumber >> 8)];
	crc = crc8_table[crc ^ (packetNumber & 0xFF)];

	crc = crc8_table[crc ^ b0];
	crc = crc8_table[crc ^ b1];
	crc = crc8_table[crc ^ b2];
	crc = crc8_table[crc ^ b3];
	crc = crc8_table[crc ^ b4];
	crc = crc8_table[crc ^ b5];
	crc = crc8_table[crc ^ b6];
	crc = crc8_table[crc ^ b7];
	crc = crc8_table[crc ^ b8];
	crc = crc8_table[crc ^ b9];

	writeByte(0x13);
	writeByte(0x37);

	writeByte(packetNumber >> 8);
	writeByte(packetNumber & 0xFF);

	writeByte(b0);
    sPacketBuffer[packetNumber].payload[0] = b0;
	writeByte(b1);
    sPacketBuffer[packetNumber].payload[1] = b1;
	writeByte(b2);
    sPacketBuffer[packetNumber].payload[2] = b2;
	writeByte(b3);
    sPacketBuffer[packetNumber].payload[3] = b3;
	writeByte(b4);
    sPacketBuffer[packetNumber].payload[4] = b4;
	
	writeByte(b5);
    sPacketBuffer[packetNumber].payload[5] = b5;
	writeByte(b6);
    sPacketBuffer[packetNumber].payload[6] = b6;
	writeByte(b7);
    sPacketBuffer[packetNumber].payload[7] = b7;
	writeByte(b8);
    sPacketBuffer[packetNumber].payload[8] = b8;
	writeByte(b9);
    sPacketBuffer[packetNumber].payload[9] = b9;

	writeByte(crc);
    sPacketBuffer[packetNumber].crc = crc;
}

